     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $receive=$request->all();
        $this->validate($request, [
            'choose_type' => 'required',
            'full_name' => 'required',
            'email' => 'required',
            'reg_no' => 'required',
            'subject' => 'required',
            'inquiry' => 'required',

        ]);
        Feedback::create($receive);
        return redirect('/')->with('success','Feedback will be submitted successfully');
    }

